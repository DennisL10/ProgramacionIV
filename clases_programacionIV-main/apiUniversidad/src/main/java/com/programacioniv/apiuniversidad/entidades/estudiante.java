package com.programacioniv.apiuniversidad.entidades;

public class estudiante {
    private int id;
    private String nombre;

    public estudiante() { }
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
